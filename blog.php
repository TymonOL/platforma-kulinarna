    <h2>Składniki</h2>
    <li>300 g mięsa mielonego (może być wieprzowe lub wołowe)</li>
    <li>300 g sosu pomidorowego (ewentualnie puszka pomidorów lub kupny sos – passata pomidorowa)</li>
    <li>2 łyżki koncentratu pomidorowego</li>
    <li>300 g makaronu dobrej jakości (waga suchego w opakowaniu przed ugotowaniem)</li>
    <li>1 cebula</li>
    <li>ząbek czosnku</li>
    <li>1 lub 2 łyżki tłuszczu do podsmażenia cebulki z czosnkiem (oliwa, olej lub smalec gęsi)</li>
    <li>do smaku po pół łyżeczki: soli, oregano, tymianku, słodkiej papryki</li>
    <li>1/4 łyżeczki świeżo zmielonego pieprzu czarnego</li>
    <section>Dodatkowe Informacje</section>
    <li>Idealne jest połączenie 1/3 mięsa wieprzowego (np łopatki lub szynki) oraz 2/3 mięsa wołowego (np ligawy).
      Najlepsze jest świeżo zmielone mięso na średnich oczkach w domu lub w sklepie przy nas. Odradzam gotowe i
      zapakowane mięso mielone, które jest tłuste i kiepskiej jakości.
      Możesz użyć dowolnego makaronu, chociaż najpopularniejszy i najwłaściwszy jest cienki oraz długi – typowy do
      spaghetti. Zobacz także jak zrobić pulpety w sosie pomidorowym.</li>
    <h2>Sposób Przygotowania</h2>
    <li>Krok 1.
      Cebulę obieram i kroję w drobną kostkę. Na dużą i rozgrzaną patelnię daję 1 lub 2 łyżki tłuszczu (u mnie 1 łyżka
      smalcu gęsiego). Wrzucam pokrojoną cebulkę i podsmażam na średnim ogniu przez około 6 minut, aż się zeszkli. Pod
      sam koniec wrzucam posiekany ząbek czosnku.</li>
    <li>Krok 2.
      Następnie do zeszklonej cebulki z czosnkiem wrzucam mięso mielone. Za pomocą drewnianej łyżki rozdrabniam
      grubsze grudki mięsa na mniejsze kawałki. Smażę tak na średnim ogniu co jakiś czas mieszając, aż mięso zmieni
      kolor. Do ściętego mięsa mielonego wlewam domowy sos pomidorowy (jeśli nie masz to użyj gotowego sosu ze sklepu
      lub pomidory z puszki – w razie potrzeby dodaj bulionu warzywnego lub wody). Dodaję 2 łyżki koncentratu
      pomidorowego, który nada pięknego koloru oraz kwaskowatego smaku.Zmniejszam ogień na mniejszy i całość mieszam
      ze sobą używając np drewnianej łyżki. Duszę pod przykryciem na niedużym ogniu przez 45 minut co jakiś czas
      mieszając. Pod koniec dodaję przyprawy: sól, pieprz, słodką paprykę, tymianek, i oregano. Mieszam, próbuję smak
      i w razie potrzeby doprawiam lub dodaję 50 – 100 ml wody bądź bulionu warzywnego. Im dłużej będziesz dusić sos
      na małym ogniu, tym będzie oczywiście lepszy. Włosi potrafią dusić sos do spaghetti nawet do 4 godzin!</li>
    <li>Krok 3.
      Pod koniec gotowania sosu mięsno pomidorowego, nastawiam w garnku wodę na makaron. Do gotowania makaronu używam
      proporcji: 10g soli na 1 litr wody i 100 g makaronu. Do osolonego wrzątku wrzucam makaron i gotuję al dente,
      czyli o 1 minutę krócej niż wynika z instrukcji na opakowaniu. Ugotowany makaron odcedzam na durszlaku.Dla osób
      zwracających uwagę na dietetykę przypomnę, że makaron ugotowany al dennte ma mniejszy indeks glikemiczny.</li>
    <li>Krok 4.
      Porcje makaronu możesz oddzielnie podać na talerzu i polać na wierzchu sosem mięsno pomidorowym. Możesz także
      jak ja wymieszać wszystko w dużej misce i podać na stół. Pierwsza metoda sprawdza mi się gdy chcę następnego
      dnia wykorzystać sos mięsno pomidorowy do nowego obiadu. Drugą metodę stosuję, gdy nie zamierzam odgrzewać tego
      dania na drugi dzień.</li>
